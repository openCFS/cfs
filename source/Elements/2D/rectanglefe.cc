  void RectangleFE::GetLocalIntPoints4Surface(const StdVector<UInt> & surfConnect,
                                              const StdVector<UInt> & volConnect,
                                              const Vector<Double> & surfIntPoint,
                                              Vector<Double> & volIntPoint)
  {
  
    // Try to find out, which vertices are in common with
    // the surface element. Then calculate the product of both
    // and compare them
    //
    //      eta
    //       ^
    // 4 +---|---+ 3    
    //   |   |   |      
    //   |   0---|-> xi     REFERENCE VOLUME ELEMENT
    //   |       |
    // 1 +-------+ 2



    StdVector<UInt> commonIndex(2);
    UInt found = 0;
    UInt indexProduct = 0;
    std::string errMsg;
  
    volIntPoint.Resize(2);
  
    // loop over surface connect
    for (UInt iSurf=0; iSurf<2; iSurf++)
      // loop over volume connect
      for (UInt iVol=0; iVol<4; iVol++)
        if (surfConnect[iSurf] == volConnect[iVol])
          {
            commonIndex[found++] = iVol+1;
          }

    indexProduct= commonIndex[0] * commonIndex[1];
    switch(indexProduct)
      {
      case 2:
        // Edge[1,2] is common
        volIntPoint[0] = surfIntPoint[0];
        volIntPoint[1] = -1.0;
        break;

      case 12:
        // Edge[4,3] is common
        volIntPoint[0] = surfIntPoint[0];
        volIntPoint[1] = 1.0;
        break;

      case 4:
        // Edge[1,4] is common
        volIntPoint[0] = -1.0;
        volIntPoint[1] = surfIntPoint[0];
        break;

      case 6:
        // Edge[2,3] is common
        volIntPoint[0] = 1.0;
        volIntPoint[1] = surfIntPoint[0];
        break;

      default:
        EXCEPTION( "RectangleFE::GetLocalIntPoints4Surface: surface and volume element "
                   <<  "have not two nodes in common. Check your .mesh-file.");
      }
  }
  
  void RectangleFE::CoordsInsideElem(const Matrix<Double> & localCoords,
                                   const Double tolerance,
                                   StdVector<bool> & coordsInside) const
  {
    UInt numPoints = localCoords.GetNumCols();
    double xi, eta;

    coordsInside.Resize(numPoints);
    
    for(UInt i=0; i<numPoints; i++)
    {
      xi = localCoords[0][i];
      eta = localCoords[1][i];

      coordsInside[i] = ( xi >= (-1.0 - tolerance)) &&
                        (eta >= (-1.0 - tolerance)) &&
                        ( xi <= (1.0 + tolerance)) &&
                        (eta <= (1.0 + tolerance));  
    }
    
  }

  void RectangleFE::GetMaxMinEdgeLength( Matrix<Double> &ptCoord, Double &max, Double &min )
  {
    max = 1e-100;
    min = 1e+100;

    for (UInt i=0; i<4; i++ )
    {
      UInt other = i < 3 ? i+1 : 0; // wrap around for last edge
      Double dx = ptCoord[0][i] - ptCoord[0][other];
      Double dy = ptCoord[1][i] - ptCoord[1][other];
      Double l  = sqrt(dx*dx + dy*dy);
      max = max > l ? max : l;
      min = min < l ? min : l;
    } 
  }  
  
  void RectangleFE::GetEdgeLength(Matrix<Double> &ptCoord, StdVector<Double>& edges_out) 
  {
    edges_out.Resize(2, 0.0);
 
    // todo: works only for aligned elements!
    
    for (UInt i=0; i<2; ++i)
    {
      // for all dimensions, add offset; only in one direction this is not 0
      edges_out[i]  = abs(ptCoord[i][0] - ptCoord[i][1]);
      edges_out[i] += abs(ptCoord[i][0] - ptCoord[i][3]);
    }
  }
  
  
