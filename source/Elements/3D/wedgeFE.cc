  /** calculate elements via the cartesian product (Solin, Segeth, Dolezel, Higher-Order Finite Element Methods, p. 250 */
  void WedgeFE::CreateHigherOrderElements()
  {
          for(int order = 1; order < 8; order++)
          {
              Line1FE*     line = new Line1FE(ECONOMICAL, order);

              Triangle1FE* tria = new Triangle1FE(ECONOMICAL, order);

              int total = line->GetNumIntPoints() * tria->GetNumIntPoints();

              std::cout << "// Cartesian product rule applied on ECONOMICAL values as given by\n";
              std::cout << "// Solin, Segeth, Dolezel, Higher-Order Finite Element Methods!! p.250\n";
              std::cout << "static Double a" << order << "[][4] = { \n";

              for(UInt i = 0; i < line->GetNumIntPoints(); i++)
              {
                for(UInt j = 0; j < tria->GetNumIntPoints(); j++)
                {

                     std::cout.precision(15); 
                     std::cout << "    {" << tria->GetIntPoints()[j][0] << ",  " 
                                          << tria->GetIntPoints()[j][1] << ",  "
                                          << line->GetIntPoints()[i][0] << ",  ";
                     Double weight = line->GetIntWeights()[i] * tria->GetIntWeights()[j];
                     std::cout << weight << " },\n";

                }
             }
             std::cout << "};\nAddIntegrationPoints(CLASSICAL, " << order << ", " << total << ", (Double*) a" << order << ");\n\n";                     
 
             delete tria;
             delete line;
           }
  }


