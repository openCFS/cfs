class Marching_Cube():
  def __init__(self,voxels,h,blb,size,offset,regular=True):
    self.regular = True
    self.size = size # edge length in one dimension
    self.h = h
    assert(size > 0)
    self.blb = blb # bottom-left-back (bottom-south-west) voxel coordinate  
    assert(len(blb) == 3)
    ncorners = 8
    self.corners = [None] * ncorners
    self.offset = offset if offset is not None else (0,0,0)
    self.global_blb = np.array(self.blb) + np.array(self.offset)
    # bottom/top face:
    # NW  ----------- NE  
    #  |              |   ^ 
    #  |              |   |
    #  |              |   |
    #  |              |   | j
    #  |              |   ------>i
    # SW  ----------- SE
    
    i = blb[0]
    j = blb[1]
    k = blb[2]
    bottom_SW = (i,j,k)
    bottom_SE = (i+size,j,k)
    bottom_NW = (i,j+size,k)
    bottom_NE = (i+size,j+size,k)
    top_SW = (i,j,k+size)
    top_SE = (i+size,j,k+size)
    top_NW = (i,j+size,k+size)
    top_NE = (i+size,j+size,k+size)
    
    self.cornerIndizes = [bottom_SW,bottom_SE,bottom_NW,bottom_NE,top_SW,top_SE,top_NW,top_NE]
    self.cornerNormals = calc_corner_normals(voxels, self.cornerIndizes,self.size)
      
    assert(len(self.cornerIndizes) == ncorners)
    for i in range(ncorners):
      cidx = self.cornerIndizes[i]
      gidx = np.array(cidx) + np.array(offset)
      self.corners[i] = Vertex(voxels[tuple(cidx)],voxel_to_cartesian_coords(gidx.astype(int),(0,0,0),h))
    
    self.caseCode = self.calc_case_code_regular(voxels)  
    
  def calc_case_code_regular(self,voxels,thresh=0.5):
    caseCode = 0
    caseCode |= 1 if voxels[self.cornerIndizes[0]] > thresh else 0
    caseCode |= 2 if voxels[self.cornerIndizes[1]] > thresh else 0
    caseCode |= 4 if voxels[self.cornerIndizes[2]] > thresh else 0
    caseCode |= 8 if voxels[self.cornerIndizes[3]] > thresh else 0
    caseCode |= 16 if voxels[self.cornerIndizes[4]] > thresh else 0
    caseCode |= 32 if voxels[self.cornerIndizes[5]] > thresh else 0
    caseCode |= 64 if voxels[self.cornerIndizes[6]] > thresh else 0
    caseCode |= 128 if voxels[self.cornerIndizes[7]] > thresh else 0
  
    return caseCode  
  
  def triangulate(self,voxels):
    # regular case
    self.points, self.triangles, self.normals, self.angles, _ = triangulate_scaled(voxels,self,scale=0)
  
# for a given voxel coordinate tuple (i,j,k),
# return a list with bottom-left-back coordinates of the (6) adjacent cubes
# size: size of MC or step length to next cube
def neighbor_face_indices(idx,size):  
  inds = [None] * 6
  i = idx[0]
  j = idx[1]
  k = idx[2]
  # order: left, right, back, front, bottom, top
  return [(i-size,j,k),(i+size,j,k),(i,j-size,k),(i,j+size,k),(i,j,k-size),(i,j,k+size)]

