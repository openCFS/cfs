# -> marching cubes grid is shifted by hx/2,hy/2,hz/2 and 1 elem smaller in each dim
# for each of the 8 lattice node, find out which ones belong to the structure
# -> 8 bit code           
def marching_cubes(voxels,spacing,points,triangles,normals,thresh = 0.5,cubeSize=2,offset=None):
  from draw_profile_functions import grid_to_cartesian_coords
  hx = spacing[0]
  hy = spacing[1]
  hz = spacing[2]
  nx, ny, nz = voxels.shape
  ntriangles = 0
  print("cubesize:",cubeSize)
  
  #edgeTable, triTable = create_lookup()
  if regularCellClass is None:
    create_transvoxel_tables()
    
  if offset is None:
    offset = np.array([0,0,0])
    
  assert(regularCellClass is not None)
  assert(regularCellData is not None)
  assert(regularVertexData is not None)    
  
  assert (0 < hx and 0 < hy and 0 < hz)
  for i in range(0,int(nx-cubeSize),cubeSize):
    for j in range(0,int(ny-cubeSize),cubeSize):
      for k in range(0,int(nz-cubeSize),cubeSize):
        
        # bottom/top face:
        # NW  ----------- NE  
        #  |              |   ^ 
        #  |              |   |
        #  |              |   |
        #  |              |   | j
        #  |              |   ------>i
        # SW  ----------- SE   
        bottom_SW = (i,j,k)
        bottom_SE = (i+cubeSize,j,k)
        bottom_NW = (i,j+cubeSize,k)
        bottom_NE = (i+cubeSize,j+cubeSize,k)
        top_SW = (i,j,k+cubeSize)
        top_SE = (i+cubeSize,j,k+cubeSize)
        top_NW = (i,j+cubeSize,k+cubeSize)
        top_NE = (i+cubeSize,j+cubeSize,k+cubeSize)
        
        cornerIndizes = [bottom_SW,bottom_SE,bottom_NW,bottom_NE,top_SW,top_SE,top_NW,top_NE]
        
        local_verts = []
        local_verts.append(Vertex(0,grid_to_cartesian_coords(bottom_SW,None,(hx,hy,hz)),voxels[bottom_SW]))
        local_verts.append(Vertex(1,grid_to_cartesian_coords(bottom_SE,None,(hx,hy,hz)),voxels[bottom_SE]))
        local_verts.append(Vertex(2,grid_to_cartesian_coords(bottom_NW,None,(hx,hy,hz)),voxels[bottom_NW]))
        local_verts.append(Vertex(3,grid_to_cartesian_coords(bottom_NE,None,(hx,hy,hz)),voxels[bottom_NE]))
        local_verts.append(Vertex(4,grid_to_cartesian_coords(top_SW,None,(hx,hy,hz)),voxels[top_SW]))
        local_verts.append(Vertex(5,grid_to_cartesian_coords(top_SE,None,(hx,hy,hz)),voxels[top_SE]))
        local_verts.append(Vertex(6,grid_to_cartesian_coords(top_NW,None,(hx,hy,hz)),voxels[top_NW]))
        local_verts.append(Vertex(7,grid_to_cartesian_coords(top_NE,None,(hx,hy,hz)),voxels[top_NE]))
        
        for l in range(len(local_verts)):
          local_verts[l].coords += grid_to_cartesian_coords(offset,None,(hx,hy,hz))
        
        # example: vertex 3 was below the isosurface, cubeindex would equal 0000 1000 or 8
        caseCode = 0
        caseCode |= 1 if local_verts[0].value > thresh else 0
        caseCode |= 2 if local_verts[1].value > thresh else 0
        caseCode |= 4 if local_verts[2].value > thresh else 0
        caseCode |= 8 if local_verts[3].value > thresh else 0
        caseCode |= 16 if local_verts[4].value > thresh else 0
        caseCode |= 32 if local_verts[5].value > thresh else 0
        caseCode |= 64 if local_verts[6].value > thresh else 0
        caseCode |= 128 if local_verts[7].value > thresh else 0
        
        if caseCode == 0 or caseCode == 255: # in cases 00000000 and 11111111 we have no triangulation
          continue
        
        cornerNormals = calcCornerNormals(voxels, cornerIndizes)
        
        cellClass = regularCellClass[caseCode] # 16 cell classes for MC
        #16-bit number, ignore high nibble
        #The low byte contains the indexes for the two endpoints of the edge on which the vertex lies
        vertexLocations = regularVertexData[caseCode] # info on end vertices of an edge
        # high nibble is vertex count, low nibble is triangle count.
        regularCell = regularCellData[cellClass] # gives triangulation within marcing cube
        
        # how many vertices?
        vertexCount = regularCell[0] >> 4
        # how many triangles? 0x0F -> 00001111
        triangleCount = regularCell[0] & 0x0F
        indices = regularCell[1]
        
        tmp_points = []
        tmp_triangles = []
        tmp_normals = []
        mappedIndizes = [None] * len(indices) # map from point id to real id
        for vc in range(vertexCount):
          v0 = (vertexLocations[vc] >> 4) & 0x0F #First Corner Index
          v1 = (vertexLocations[vc]) & 0x0F # Second Corner Index
          
          offset_p = grid_to_cartesian_coords(offset,None,(hx,hy,hz))
          vert, mu = VertexInterp(thresh,local_verts[v0],local_verts[v1])
          tmp_points.append(vert)
          normal = cornerNormals[int(v0)] * (1-mu) + mu * cornerNormals[int(v1)]
          tmp_normals.append(normal)
          
          mappedIndizes[vc] = len(points)-1+len(tmp_points)
        
        # for each triangle, store n0,n1 and n2  
        n = []  
        for t in range(triangleCount):
          triangle = []
          # vertex normals of this triangle
          tn = []
          for c in range(3):
            triangle.append(mappedIndizes[indices[t*3+c]])
            tn.append(tmp_normals[indices[t*3+c]])
          tmp_triangles.append(triangle)
          n.append(tn)
        
        angles = []  
        for t in range(len(n)):
          n0, n1, n2 = n[t]

          angles.append(calcAngle(n0, n1))
          angles.append(calcAngle(n0, n2))
          angles.append(calcAngle(n1, n2))
          
#           print("\nangle between ",n0,n1," is ",calcAngle(n0, n1))
#           print("angle between ",n0,n2," is ",calcAngle(n0, n2))
#           print("angle between ",n1,n2," is ",calcAngle(n1, n2))
          
        assert(len(angles) > 0)
        if cubeSize == 1 or np.any(np.array(angles) < 30):
          points.extend(tmp_points)
          triangles.extend(tmp_triangles)
          normals.extend(tmp_normals)
        else:
          print(angles)
          marching_cubes(voxels[i:i+cubeSize+1,j:j+cubeSize+1,k:k+cubeSize+1], (hx,hy,hz), points, triangles, normals, thresh, int(cubeSize/2),offset=(i,j,k))
            
  
  if cubeSize == 2:
    print("points:",len(points))
    print("triangles:",len(triangles))      
    write_vtp(points, triangles, (hx,hy,hz),normals=normals)
  
  return points, triangles
  
# linearly interpolate intersection points with isovalue 'thresh'
# p1 and p2 are vertices of an edge and v1 and v2 scalar values at respective vertex
# intersection point P = p1 + (thresh - v1) * (p2-p1) / (v2-v1)
def VertexInterp(thresh,p1,p2):
  
  if smaller(p2,p1):
    # swap p1 and p2
    p1, p2 = p2, p1
  
  valp1 = p1.value
  valp2 = p2.value
  p1 = np.asarray(p1.coords)
  p2 = np.asarray(p2.coords)  
    
  if np.isclose(valp2-valp1,0):
    return p1
  else:
    mu = (thresh - valp1) / (valp2 - valp1)  
    return p1 + mu * (p2 - p1), mu  
    
# kind of comparing point with descending priority for x to z 
def smaller(p1,p2):
  p1 = p1.coords
  p2 = p2.coords
  if p1[0] < p2[0]:
    return True
  elif p1[0] > p2[0]:
    return False
  
  if p1[1] < p2[1]:
    return True
  elif p1[1] > p2[1]:
    return False
  
  if p1[2] < p2[2]:
    return True
  elif p1[2] > p2[2]:
    return False
  
  return False  

