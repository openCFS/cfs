# linearly interpolate intersection points with isovalue 'thresh'
# p1 and p2 are vertices of an edge and v1 and v2 scalar values at respective vertex
# intersection point P = p1 + (thresh - v1) * (p2-p1) / (v2-v1)
def VertexInterp(thresh,p1,p2):
  
  if smaller(p2,p1):
    # swap p1 and p2
    p1, p2 = p2, p1
  
  valp1 = p1.value
  valp2 = p2.value
  p1 = np.asarray(p1.coords)
  p2 = np.asarray(p2.coords)  
  
  if np.isclose(valp2-valp1,0):
    return p1, 0
  else:
    mu = (thresh - valp1) / (valp2 - valp1)  
    return p1 + mu * (p2 - p1), mu  
    
# kind of comparing point with descending priority for x to z 
def smaller(p1,p2):
  p1 = p1.coords
  p2 = p2.coords
  if p1[0] < p2[0]:
    return True
  elif p1[0] > p2[0]:
    return False
  
  if p1[1] < p2[1]:
    return True
  elif p1[1] > p2[1]:
    return False
  
  if p1[2] < p2[2]:
    return True
  elif p1[2] > p2[2]:
    return False
  
  return False  

# returns list with 7 entries with flags (0 or 1) for which cube face transition from low-res to
# high-res cell takes place - entries are of type Cube_faces/ints
# neglect first entry as it stand for no transition at all
# input: bottom-left-back coord of low-res cell and high-res cell
def calc_transition_flags(lowres,highres):
  print("transition from ", lowres, " to ", highres)
  diff = np.array(highres) - np.array(lowres)
  signs = [np.sign(d) if d != 0 else 0 for i,d in enumerate(diff.astype(int))]
  
  if not np.any(signs):
    print("here should be a transition but none found!")
    sys.exit()
  
  res = np.zeros(7)
  if signs[0] > 0:
    res[Cube_face.right] = 1
  elif signs[0] < 0:
    res[Cube_face.left] = 1
  
  if signs[1] > 0:
    res[Cube_face.back] = 1   
  elif signs[1] < 0:
    res[Cube_face.front] = 1
  
  if signs[2] > 0:
    res[Cube_face.top] = 1   
  elif signs[2] < 0:
    res[Cube_face.bottom] = 1
    
  return res  

